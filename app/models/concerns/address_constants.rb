  STREET_ABBREVS = STREET_TYPES.keys.inject({}) { |dict, road_field|
    new_val = STREET_TYPES[road_field]
    cur_val = dict[new_val] || nil

    if cur_val.is_a? String
      dict[new_val] = [cur_val, road_field]
    elsif cur_val.is_a? Array
      dict[new_val] = cur_val << road_field
    else
      dict[new_val] = road_field
    end

    dict
  }.with_indifferent_access

